const Schema = require("mongoose").Schema;

const UserSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = (mongooseConnection) =>
  mongooseConnection.model("User", UserSchema);
